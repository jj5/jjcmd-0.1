<?php

abstract class AppSubtask extends AppTask {


  //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  // 2025-03-13 jj5 - fields...
  //

  protected AppTask $parent_task;


  //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  // 2025-03-12 jj5 - constructor...
  //

  public function __construct( AppTask $parent_task ) {

    parent::__construct();

    $this->parent_task = $parent_task;

  }


  //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  // 2025-03-12 jj5 - protected functions...
  //

  protected function define_type() : AppTaskType { return AppTaskType::Standard; }


  //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  // 2025-03-13 jj5 - functions...
  //

  public function is_subtask() : bool { return true; }

  public function get_parent_task() : AppTask {

    return $this->parent_task;

  }
}
